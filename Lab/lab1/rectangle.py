import turtle

my_turtle = turtle.Turtle()

my_turtle.forward(100)  # draw the first side
my_turtle.right(90)  # turn right 90 degrees
my_turtle.forward(50)  # draw the second side
my_turtle.right(90)
my_turtle.forward(100)  # draw the third side
my_turtle.right(90)
my_turtle.forward(50)  # draw the fourth side

turtle.done()
